    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root");
            Statement stmt = conn.createStatement();
            ResultSet rs=stmt.executeQuery("select * from student left join result using(rollno) where rollno='"+rollno+"';");
            if(rs.next()){
                this.setVisible(false);
                new StudentReport(rs).setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "No Student Exists With This Roll Number");
            }
            conn.close();
        }catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

